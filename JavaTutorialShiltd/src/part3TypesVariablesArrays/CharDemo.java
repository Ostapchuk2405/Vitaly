package part3TypesVariablesArrays;

/**
 * Демонстрация Юникода символов в программе
 * 
 * @author Shildt
 * @return Изменения символьной переменной в Юникоде
 */

public class CharDemo {

	public static void main(String[] args) {

		char ch1 = 'X';

		System.out.println("ch1 is " + ch1);

		ch1++;
		System.out.println("ch1 is " + ch1 + " now");

		ch1++;
		System.out.println("ch1 is " + ch1 + " now");
	}

}
