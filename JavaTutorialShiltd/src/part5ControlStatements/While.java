package part5ControlStatements;

/**
 * Демонстрация использования цикла while
 * 
 * @author Shiltd
 *
 */
public class While {

	/**
	 * @param args заданные циклы while
	 */
	public static void main(String[] args) {

		int n = 10;
		while (n > 0) {
			System.out.println("Такт " + n);
			n--;
		}
	}

}
